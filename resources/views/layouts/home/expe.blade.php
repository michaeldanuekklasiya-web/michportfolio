            // MOBILE: Gabungkan semua experience card & sembunyikan tombol "See more"
            function handleMobileExperience() {
                const initial = document.getElementById('initial-experience');
                const hidden = document.getElementById('hidden-experience');
                if (window.innerWidth < 768) {
                    // Gabungkan isi initial dan hidden
                    if (hidden && initial && !initial.classList.contains('mobile-merged')) {
                        initial.innerHTML += hidden.innerHTML;
                        hidden.style.display = 'none';
                        initial.classList.add('mobile-merged');
                    }
                    // Sembunyikan tombol "See more" dan "Show less"
                    document.getElementById('learn-more-indicator').style.display = 'none';
                    document.getElementById('show-less-indicator').style.display = 'none';
                    // Kurangi gap antar card
                    initial.classList.remove('space-y-4');
                    initial.classList.add('space-y-2');
                    // Hapus animasi fade-in di semua card
                    document.querySelectorAll('.experience-card').forEach(card => {
                        card.removeAttribute('data-aos');
                        card.removeAttribute('data-aos-duration');
                    });
                } else {
                    // Kembalikan animasi fade-in di desktop
                    document.querySelectorAll('.experience-card').forEach(card => {
                        card.setAttribute('data-aos', 'fade-up');
                        card.setAttribute('data-aos-duration', '800');
                    });
                }
            }
            handleMobileExperience();
            window.addEventListener('resize', handleMobileExperience);
        });
