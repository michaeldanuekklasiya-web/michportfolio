<script>
// Simple carousel logic for testimonials (no buttons, pause on hover)
(function() {
    const carousel = document.getElementById('testimonials-carousel');
    const cards = carousel.querySelectorAll('.testimonial-card');
    let current = 0;
    let interval = null;
    let paused = false;
    const getVisibleCards = () => window.innerWidth >= 1024 ? 4 : (window.innerWidth >= 768 ? 2 : 1);

    function showCards() {
        const visibleCards = getVisibleCards();
        cards.forEach((card, i) => {
            if (i >= current && i < current + visibleCards) {
                card.classList.add('active');
            } else {
                card.classList.remove('active');
            }
        });
    }

    function next() {
        const visibleCards = getVisibleCards();
        current = (current + 1) % (cards.length - visibleCards + 1);
        showCards();
    }
    function startAuto() {
        if (interval) clearInterval(interval);
        interval = setInterval(() => {
            if (!paused) next();
        }, 3000);
    }
    function pauseAuto() { paused = true; }
    function resumeAuto() { paused = false; }

    // Init
    showCards();
    startAuto();

    // Pause on hover any card
    cards.forEach(card => {
        card.addEventListener('mouseenter', pauseAuto);
        card.addEventListener('mouseleave', resumeAuto);
    });

    // Responsive: update visibleCards on resize
    window.addEventListener('resize', () => {
        showCards();
    });
})();
</script>

